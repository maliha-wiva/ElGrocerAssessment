/// A model representing a category displayed in the home screen.
///
/// Each category has a unique identifier, a display name, and an image URL for UI presentation.
struct Category {
    /// The unique identifier for the category.
    let id: Int
    /// The display name of the category.
    let name: String
    /// The URL string of the category image.
    let imageUrl: String
}
